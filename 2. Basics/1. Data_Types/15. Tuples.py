# Tuple - are like lists but its immutable, we can't change or modify them.

my_tuples = (1,2,3,4,5,6)
print(5 in my_tuples)

# Methods in Tuple

# count() - how many given elements in tuple
print(my_tuples.count(5))

# index() - index no. of given element
print(my_tuples.index(2))

# len() - length of tuple
print(len(my_tuples))